#ifndef _STDINT_H
#define _STDINT_H

// ================================================================================================================================
// ================================================================================================================================

#ifdef __UINT8_TYPE__
typedef __UINT8_TYPE__ uint8_t;
#endif
#ifdef __UINT16_TYPE__
typedef __UINT16_TYPE__ uint16_t;
#endif
#ifdef __UINT32_TYPE__
typedef __UINT32_TYPE__ uint32_t;
#endif
#ifdef __UINT64_TYPE__
typedef __UINT64_TYPE__ uint64_t;
#endif

#ifdef __INT8_TYPE__
typedef __INT8_TYPE__ int8_t;
#endif
#ifdef __INT16_TYPE__
typedef __INT16_TYPE__ int16_t;
#endif
#ifdef __INT32_TYPE__
typedef __INT32_TYPE__ int32_t;
#endif
#ifdef __INT64_TYPE__
typedef __INT64_TYPE__ int64_t;
#endif

// ================================================================================================================================
// ================================================================================================================================

typedef uint64_t  QWORD,   qword;
typedef uint32_t  DWORD,   dword;
typedef uint16_t  WORD,    word;
typedef uint8_t   BYTE,    byte;

typedef int64_t   INT64,   int64;
typedef int32_t   INT32,   int32;
typedef int16_t   INT16,   int16;
typedef int8_t    INT8,    int8;

// ================================================================================================================================
// ================================================================================================================================

#endif // _STDINT_H

